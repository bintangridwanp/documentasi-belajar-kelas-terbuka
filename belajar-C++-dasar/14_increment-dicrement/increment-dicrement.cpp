#include <iostream>
using namespace std;

int main (){

    int a = 10;
    int b = 10;
    int c = 20;
    int d = 20;

    cout << a << endl;
    cout << a++ << endl;
    cout << a << endl;

    cout << " " << endl;

    cout << b << endl;
    cout << ++b << endl;
    cout << b << endl;

    cout << " " << endl;

    cout << c << endl;
    cout << c-- << endl;
    cout << c << endl;

    cout << " " << endl;

    cout << d << endl;
    cout << --d << endl;
    cout << d << endl;

}