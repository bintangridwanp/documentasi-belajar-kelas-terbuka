#include <iostream>

using namespace std;

int main() {
    int a = 10;
    int b = 5;

    int hasil = 0;

    hasil = a + b;
    cout << a << " + " << b << " = " << hasil << endl;

    hasil = a - b;
    cout << a << " - " << b << " = " << hasil << endl;

    hasil = a * b;
    cout << a << " * " << b << " = " << hasil << endl;

    hasil = a / b;
    cout << a << " / " << b << " = " << hasil << endl;

    hasil = a % b;
    cout << a << " % " << b << " = " << hasil << endl;

    cin.get();
    return 0;
}
