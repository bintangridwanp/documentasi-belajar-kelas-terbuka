#include <iostream>

using namespace std;

int main() {
    int a = 10;
    int b = 20;

    // membandingkan apakah a dan b sama
    cout << (a == b) << endl;

    // membandingkan apakah a dan b tidak sama
    cout << (a != b) << endl;

    // membandingkan apakah a lebih kecil dari b
    cout << (a < b) << endl;

    // membandingkan apakah a lebih besar dari b
    cout << (a > b) << endl;

    // membandingkan apakah a lebih kecil atau sama dengan b
    cout << (a <= b) << endl;

    // membandingkan apakah a lebih besar atau sama dengan b
    cout << (a >= b) << endl;

    return 0;
}
